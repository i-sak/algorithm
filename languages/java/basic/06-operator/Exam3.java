
public class Exam3 {
	public static void main(String[] args) {
		int a = 100;
		System.out.println("a = " + a);
		
		a = a + 100;
		System.out.println("a = " + a);
		
		a += 100;
		System.out.println("a = " + a);
		
		a -= 50;
		System.out.println("a = " + a);
		
		a *= 2;
		System.out.println("a = " + a);
		
		a /= 100;
		System.out.println("a = " + a);
		
		a %= 2; // a = a % 2;
		System.out.println("a = " + a);
		
		
	}
}
